package Ex03;


public class Pessoa {
	
	private String name;
	private String cargo;

	
	public Pessoa(String name, String cargo) {
		super();
		this.name = name;
		this.cargo = cargo;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}

	
	
	

}
